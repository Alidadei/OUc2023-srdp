/**************************************************************************************
实验名称：智能感应声温控风扇

接线说明：参考实验现象仿真电路
		  	
实验现象：系统运行后，LCD1602显示传感器检测的温度值、人体感应信号、声音信号状态；
		  默认以自动模式运行，若按下K4键切换为手动模式，此时可以
		  通过K1和K2控制风扇加减速，风扇档位共5档（0-5）。当再次按下K4键切换为自动
		  模式；自动模式下，当人体红外检测到人或检测到有声音时，
		  当温度高于上限且在2C范围内，以3档运行。
		  当温度高于上限且在4C范围内，以4档运行。当温度高于上限且在6C范围内，以5挡
		  运行。当温度高于上限且超过6C范围，以5档运行。
		  若温度在上下限范围内，风扇以2档运行。
		  若温度低于下限，风扇停止。当人体红外检测无人且未检测到有声音时，关闭风扇；
		  可通过K3键进入阈值设置，K1和K2调节。
			
注意事项：
		  																				  
***************************************************************************************/

